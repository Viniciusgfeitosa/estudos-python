# > Operacoes Matematicas (Aritmeticas)

"""
Operadores Aritméticos:
+ Adição
- Subtração
* Multiplicação
/ Divisão
% Resto da divisão
** Exponenciação
// Divisão inteira (arredonda para baixo)
"""

numero1 = 10
numero2 = 20

print(numero1 + numero2) # Saída: 30
print(numero1 - numero2) # Saída: -10
print(numero1 * numero2) # Saída: 200
print(numero1 / numero2) # Saída: 0.5    
print(numero1 // numero2) # Saída: 0    
print(20 // 3) # Saída: 6
print(20 % 3) # Saída: 2
print(2 ** 3) # Saída: 8

# Operadores booleanos

idade1 = 10
idade2 = 15
altura1 = 1.77
altura2 = 1.65
altura3 = altura2

print(idade1 > idade2) # Saída: False
print(idade1 <= idade2) # Saída: True
print('Python' == 'python') # Saída: False
print('banana' != 'abacaxi') # Saída: True
print(altura1 >= altura2) # Saída: True
print(altura2 > altura3) # Saída: False